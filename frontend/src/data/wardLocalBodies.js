// Which dataset key backs a given position (null → Panchayat Union / all districts).
export function datasetKeyForPosition(position) {
  if (position === 'Corporation') return 'corporations';
  if (position === 'Municipality') return 'municipalities';
  if (position === 'Town Panchayat') return 'town_panchayats';
  return null;
}
